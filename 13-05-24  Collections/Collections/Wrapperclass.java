public class Wrapperclass {
    public static void main(String[] args) {
      
        Integer obj1 = Integer.valueOf(5);
        Double obj2 = Double.valueOf(5.55);
        int a = obj1.intValue();
        double b = obj2.doubleValue();
        System.out.println( a);
        System.out.println(b);
            
          
          
    }
    
}
